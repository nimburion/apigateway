function detectLocale(): Locale {
  if (typeof window !== 'undefined') {
    const stored = window.localStorage.getItem(localeStorageKey)
    if (stored === 'en' || stored === 'it') {
      return stored
    }
  }
  if (typeof navigator === 'undefined') {
    return 'en'
  }
  return 'en'
}

let currentLocale: Locale = detectLocale()

export function getLocale(): Locale {
  return currentLocale
}

export function setLocale(locale: Locale) {
  currentLocale = locale
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(localeStorageKey, locale)
  }
  if (typeof document !== 'undefined') {
    document.documentElement.lang = locale
  }
}

export function t(key: string, params?: Record<string, string | number>): string {
  const table = messages[currentLocale] ?? messages.en
  let value = table[key] ?? messages.en[key] ?? key
  if (!params) {
    return value
  }
  for (const [name, param] of Object.entries(params)) {
    value = value.split(`{${name}}`).join(String(param))
  }
  return value
}

export function tCount(baseKey: string, count: number): string {
  const key = count === 1 ? baseKey : `${baseKey}_plural`
  return t(key, { count })
}
